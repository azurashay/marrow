const CHANNEL_FIT: Record<ChannelId, Draft['kind'][]> = {
  linkedin: ['post', 'carousel'],
  instagram: ['post', 'reel', 'carousel'],
  facebook: ['post'],
  tiktok: ['reel'],
  youtube: ['reel'],
}

export function generateDrafts(soul: Soul, channels: ChannelId[], count = 6): Draft[] {
  const pool = TEMPLATES[soul.goal]
  const out: Draft[] = []
  let i = 0
  while (out.length < count && i < count * 4) {
    const ch = channels[i % channels.length]
    const t = pool[(i + out.length) % pool.length]
    i++
    if (!CHANNEL_FIT[ch].includes(t.kind)) continue
    out.push({
      id: uid(),
      channel: ch,
      kind: t.kind,
      hook: t.hook(soul),
      body: t.body(soul),
      hashtags: t.tags(soul),
      status: 'pending',
      createdAt: Date.now() - out.length * 1000,
    })
  }
  return out
}

